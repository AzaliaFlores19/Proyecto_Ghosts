    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
         if (txtUsername.getText().isEmpty() || txtPassword.getText().isEmpty() ) {
        JOptionPane.showMessageDialog(null, "Llene todos los campos.");
    } else {
           String username = txtUsername.getText();
           String password = txtPassword.getText();
           String eliminar = txtUsername.getText()+"(-Eliminada)-";
           
                 
            // Buscar el usuario en los datos existentes
            Usuarios1 usuario = login.getDatos().buscarUsuario(username);
            
            if (usuario!= null) {
                if(username.equals(String.valueOf(this.datos.UsuarioLogeado))){
                if (usuario.getPassword().equals(password)) {
                    datos.eliminarUsuario(usuario);
                    
                    JOptionPane.showMessageDialog(null, "El Usuario Fue Eliminado Exitosamente");
                    MenuInicio menuInicio=new MenuInicio(login,datos);
                   menuInicio.setVisible(true);
                   this.dispose();

                } else {
                    JOptionPane.showMessageDialog(null, "La contrase√±a actual no es correcta.");
                }
                 }else{
                       JOptionPane.showMessageDialog(null, "Debe ser el usuario LOGGED IN");  
                 }
            } else {
                JOptionPane.showMessageDialog(null, "El usuario no existe.");
            } 
         }
    }//GEN-LAST:event_btnEliminarActionPerformed

